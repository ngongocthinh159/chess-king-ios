import SwiftUI
import AVFAudio

@main
struct Tetris_MafiaApp: App {
    @State private var modelData = ModelData().loadOrInitData()
    @State private var session = SessionManager()
    
    var body: some Scene {
        WindowGroup {
            ContentView()
                .environment(self.modelData)
                .environment(self.session)
                .onAppear {
                    // Setup SessionManager for manipulating user data, modelData is the database
                    session.setup(with: modelData)
                    
                    // Setup Audio
                    AudioManager.shared.updateSettings(from: modelData)
                    setupAudioSession()
                }
                .preferredColorScheme(.dark)
        }
    }
    
    private func setupAudioSession() {
        do {
            try AVAudioSession.sharedInstance().setCategory(.playback, mode: .default)
            try AVAudioSession.sharedInstance().setActive(true)
        } catch {
            print("Failed to set up audio session: \(error)")
        }
    }
}
